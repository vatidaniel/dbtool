package io.github.vatisteve.metadata.postgres;

import io.github.vatisteve.metadata.core.ColumnMetadata;
import io.github.vatisteve.metadata.core.ReferenceActionType;
import io.github.vatisteve.metadata.core.ReferenceMetadata;
import io.github.vatisteve.metadata.core.TableMetadata;
import org.junit.jupiter.api.Test;

import java.sql.Connection;
import java.util.List;

import static org.junit.jupiter.api.Assertions.assertNotNull;
import static org.junit.jupiter.api.Assertions.assertTrue;

/**
 * @author tinhnv
 */
class PostgresDdlExecutorTest {

    /** Captures the generated SQL instead of running it, so DDL generation can be asserted without a DB. */
    private static class CapturingExecutor extends PostgresDdlExecutor {
        private String capturedSql;

        CapturingExecutor(TableMetadata tableMetadata) {
            super(tableMetadata, (Connection) null);
        }

        @Override
        public void executeSql(String sql) {
            this.capturedSql = sql;
        }
    }

    private TableMetadata personTable() {
        ColumnMetadata id = ColumnMetadata.builder()
            .name("id").dataType("INTEGER").primaryKey(true).nullable(false).identity(true)
            .build();
        ColumnMetadata name = ColumnMetadata.builder()
            .name("name").dataType("VARCHAR").dataTypeExtension("255")
            .columnDefault(ColumnMetadata.DefaultColumnValue.builder()
                .dataType(PostgresDataType.VARCHAR).value("unknown").build())
            .build();
        ColumnMetadata parentId = ColumnMetadata.builder()
            .name("parent_id").dataType("INTEGER")
            .referenceMetadata(new ReferenceMetadata("parent", "id",
                ReferenceActionType.CASCADE, ReferenceActionType.SET_NULL))
            .build();
        return TableMetadata.builder()
            .name("person")
            .tablespace("my_space")
            .columnsMetadata(List.of(id, name, parentId))
            .build();
    }

    @Test
    void createTable_usesPostgresQuotingIdentityAndTablespace() throws Exception {
        CapturingExecutor executor = new CapturingExecutor(personTable());
        executor.createTable();
        String sql = executor.capturedSql;

        assertNotNull(sql);
        assertTrue(sql.contains("CREATE TABLE \"person\""), sql);
        assertTrue(sql.contains("\"id\" INTEGER"), sql);
        assertTrue(sql.contains("GENERATED BY DEFAULT AS IDENTITY"), sql);
        assertTrue(sql.contains("\"name\" VARCHAR(255)"), sql);
        assertTrue(sql.contains("DEFAULT 'unknown'"), sql);
        assertTrue(sql.contains("PRIMARY KEY(\"id\")"), sql);
        // FK with distinct ON DELETE / ON UPDATE actions; referenced names quoted per-dialect
        assertTrue(sql.contains("FOREIGN KEY (\"parent_id\") REFERENCES \"parent\"(\"id\")"), sql);
        assertTrue(sql.contains("ON DELETE CASCADE"), sql);
        assertTrue(sql.contains("ON UPDATE SET NULL"), sql);
        // Postgres storage clause, not MySQL's "Engine ="
        assertTrue(sql.contains("TABLESPACE my_space"), sql);
        assertTrue(!sql.contains("Engine"), sql);
    }

    @Test
    void updateColumnDefinition_usesAlterColumnType() throws Exception {
        CapturingExecutor executor = new CapturingExecutor(personTable());
        executor.updateColumnDefinition("name");
        String sql = executor.capturedSql;

        assertNotNull(sql);
        assertTrue(sql.contains("ALTER TABLE person ALTER COLUMN \"name\" TYPE VARCHAR(255)"), sql);
        // must NOT use the MySQL MODIFY form
        assertTrue(!sql.contains("MODIFY"), sql);
    }
}
